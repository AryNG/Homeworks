package practicasejercicios;

public class Cuentas3en3 {

	public static void main(String[] args) {
		int numero=0;
		while (numero<300) {
			numero+=3;
			System.out.println("Serie 3 en 3: "+numero);
		}
		numero=303;
		System.out.println("\n");
		while (numero>0) {
			numero-=3;
			System.out.println("Y en reversa: " + numero);

	   }
   }
}
